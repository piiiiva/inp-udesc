
public class Exercicio1 {
   /*
   * Desenvolva um algoritmo para listar todos os números entre 0 e 10.
   */
   
    public static void main(String[] args) {
        int a = 0;
        
        while (a <= 10) {
            System.out.println("O valor de a é " + a);
            a++;
        }
    }
}